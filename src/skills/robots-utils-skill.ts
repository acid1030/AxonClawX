// ==================== 通配符工具函数 ====================

/**
 * 将路径模式转换为 robots.txt 通配符格式
 * 
 * 支持:
 * - * 匹配任意字符
 * - $ 匹配 URL 结尾
 */
export function toRobotsPattern(path: string): string {
  // 如果已经以 $ 结尾，保持不变
  if (path.endsWith('$')) {
    return path;
  }
  
  // 如果路径以 / 结尾，添加 * 匹配子路径
  if (path.endsWith('/')) {
    return `${path}*`;
  }
  
  return path;
}

/**
 * 创建精确匹配路径 (使用 $ 锚定结尾)
 */
export function exactMatch(path: string): string {
  if (path.endsWith('$')) {
    return path;
  }
  return `${path}$`;
}

/**
 * 创建前缀匹配路径
 */
export function prefixMatch(path: string): string {
  if (path.endsWith('*')) {
    return path;
  }
  if (path.endsWith('/')) {
    return `${path}*`;
  }
  return `${path}/*`;
}

/**
 * 创建包含特定字符串的匹配模式
 */
export function containsMatch(pattern: string): string {
  return `*${pattern}*`;
}

/**
 * 创建扩展名匹配模式
 */
export function extensionMatch(extension: string): string {
  if (!extension.startsWith('.')) {
    extension = `.${extension}`;
  }
  return `*${extension}$`;
}

