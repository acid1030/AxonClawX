// ==================== 实用工具 ====================

/**
 * 解析 robots.txt 内容
 */
export function parseRobotsTxt(content: string): RobotsConfig {
  const config: RobotsConfig = {
    rules: [],
    sitemap: [],
  };
  
  const lines = content.split('\n').map(line => line.trim());
  let currentRule: RobotsRule | null = null;
  
  for (const line of lines) {
    // 跳过空行和注释
    if (!line || line.startsWith('#')) {
      continue;
    }
    
    const [key, ...valueParts] = line.split(':');
    const keyLower = key.trim().toLowerCase();
    const value = valueParts.join(':').trim();
    
    switch (keyLower) {
      case 'user-agent':
        if (currentRule) {
          config.rules.push(currentRule);
        }
        currentRule = {
          userAgent: value,
          allow: [],
          disallow: [],
        };
        break;
        
      case 'allow':
        if (currentRule) {
          currentRule.allow?.push(value);
        }
        break;
        
      case 'disallow':
        if (currentRule) {
          currentRule.disallow?.push(value);
        }
        break;
        
      case 'crawl-delay':
        if (currentRule) {
          currentRule.crawlDelay = parseInt(value, 10);
        }
        break;
        
      case 'sitemap':
        config.sitemap?.push(value);
        break;
        
      case 'host':
        config.host = value;
        break;
    }
  }
  
  // 添加最后一个规则
  if (currentRule) {
    config.rules.push(currentRule);
  }
  
  return config;
}

/**
 * 合并多个 robots 配置
 */
export function mergeRobotsConfigs(...configs: RobotsConfig[]): RobotsConfig {
  const merged: RobotsConfig = {
    rules: [],
    sitemap: [],
  };
  
  for (const config of configs) {
    merged.rules.push(...config.rules);
    if (config.sitemap) {
      merged.sitemap.push(...config.sitemap);
    }
    if (config.host && !merged.host) {
      merged.host = config.host;
    }
  }
  
  return merged;
}

/**
 * 从配置生成下载链接
 */
export function getDataUri(config: RobotsConfig, options?: GenerateRobotsOptions): string {
  const content = generateRobotsTxt(config, options);
  const encoded = encodeURIComponent(content);
  return `data:text/plain;charset=utf-8,${encoded}`;
}

