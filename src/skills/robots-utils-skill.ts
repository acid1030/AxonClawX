// ==================== 核心函数 ====================

/**
 * 验证 robots 规则是否合法
 */
export function validateRobotsRule(rule: RobotsRule): boolean {
  if (!rule.userAgent || rule.userAgent.trim() === '') {
    return false;
  }
  
  // 验证通配符语法
  const validatePattern = (pattern: string): boolean => {
    // * 可以出现在任何位置
    // $ 只能出现在末尾
    if (pattern.includes('$') && !pattern.endsWith('$')) {
      return false;
    }
    return true;
  };
  
  const paths = [...(rule.allow || []), ...(rule.disallow || [])];
  for (const path of paths) {
    if (!validatePattern(path)) {
      return false;
    }
  }
  
  // crawl-delay 必须是非负数
  if (rule.crawlDelay !== undefined && rule.crawlDelay < 0) {
    return false;
  }
  
  return true;
}

/**
 * 生成单个 User-agent 的规则块
 */
export function generateRuleBlock(rule: RobotsRule, options: GenerateRobotsOptions = {}): string {
  const lines: string[] = [];
  const { addComments = false, validate = false } = options;
  
  // 验证
  if (validate && !validateRobotsRule(rule)) {
    throw new Error(`Invalid robots rule for user-agent: ${rule.userAgent}`);
  }
  
  // 注释
  if (addComments) {
    lines.push(`# Rules for ${rule.userAgent}`);
  }
  
  // User-agent
  lines.push(`User-agent: ${rule.userAgent}`);
  
  // Allow 规则
  if (rule.allow && rule.allow.length > 0) {
    for (const path of rule.allow) {
      lines.push(`Allow: ${path}`);
    }
  }
  
  // Disallow 规则
  if (rule.disallow && rule.disallow.length > 0) {
    for (const path of rule.disallow) {
      lines.push(`Disallow: ${path}`);
    }
  }
  
  // Crawl-delay
  if (rule.crawlDelay !== undefined) {
    lines.push(`Crawl-delay: ${rule.crawlDelay}`);
  }
  
  return lines.join('\n');
}

/**
 * 生成 Sitemap 行
 */
export function generateSitemapLine(sitemapUrl: string): string {
  return `Sitemap: ${sitemapUrl}`;
}

/**
 * 生成 Host 行
 */
export function generateHostLine(host: string): string {
  return `Host: ${host}`;
}

/**
 * 生成完整的 robots.txt 内容
 */
export function generateRobotsTxt(config: RobotsConfig, options: GenerateRobotsOptions = {}): string {
  const lines: string[] = [];
  const { addComments = false, validate = false } = options;
  
  // 文件头注释
  if (addComments) {
    lines.push('# robots.txt');
    lines.push(`# Generated by ACE Robots Utils`);
    lines.push(`# Generated at: ${new Date().toISOString()}`);
    lines.push('');
  }
  
  // Host (可选)
  if (config.host) {
    lines.push(generateHostLine(config.host));
    lines.push('');
  }
  
  // 规则块
  for (const rule of config.rules) {
    lines.push(generateRuleBlock(rule, options));
    lines.push('');
  }
  
  // Sitemap
  if (config.sitemap && config.sitemap.length > 0) {
    for (const sitemapUrl of config.sitemap) {
      lines.push(generateSitemapLine(sitemapUrl));
    }
  }
  
  return lines.join('\n').trim() + '\n';
}

