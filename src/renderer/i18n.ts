// Backward-compat bridge:
// Many legacy views still call keys like `settings.*` / `usage.*` under defaultNS `chat`.
// Mirror all top-level namespaces into `chat` (except `chat` itself) so language switching updates page text consistently.
function bridgeLegacyNamespaces(res: Record<string, any>): void {
  for (const lang of Object.keys(res)) {
    const langRes = res[lang] || {};
    langRes.chat = langRes.chat || {};
    const namespacesToBridge = Object.keys(langRes).filter((ns) => ns !== 'chat');
    for (const ns of namespacesToBridge) {
      if (langRes[ns] && !langRes.chat[ns]) {
        langRes.chat[ns] = langRes[ns];
      }
    }
    if (langRes.chat?.composer?.newChatShort && !langRes.chat.newChatShort) {
      langRes.chat.newChatShort = langRes.chat.composer.newChatShort;
    }
  }
}

bridgeLegacyNamespaces(resources as Record<string, any>);

